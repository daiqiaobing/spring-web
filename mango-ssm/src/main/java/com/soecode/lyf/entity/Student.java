package com.soecode.lyf.entity;

/**
 * @Author 戴礼明
 * @Date 2018/3/15 17:14
 * @Description 学生实体类
 */

public class Student {

    private int id;

    private String name;

    private int age;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
